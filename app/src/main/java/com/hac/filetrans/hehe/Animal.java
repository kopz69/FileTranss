public class Animal {
    private String aName;
    private String aSpeak;


    public Animal() {
    }

    public Animal(String aName, String aSpeak) {
        this.aName = aName;
        this.aSpeak = aSpeak;

    }

    public String getaName() {
        return aName;
    }

    public String getaSpeak() {
        return aSpeak;
    }



    public void setaName(String aName) {
        this.aName = aName;
    }

    public void setaSpeak(String aSpeak) {
        this.aSpeak = aSpeak;
    }


}